package br.com.goup.snkcustomevents.partners;

public class ParceiroContato {

    private String nome;

    private String cpf;

    private String identidade;

    private String telefone;

    private String celular;

    private String email;

    private String aceitaSms;

    private String aceitaEmail;

    private String dataNascimento;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getIdentidade() {
        return identidade;
    }

    public void setIdentidade(String identidade) {
        this.identidade = identidade;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAceitaSms() {
        return aceitaSms;
    }

    public void setAceitaSms(String aceitaSms) {
        this.aceitaSms = aceitaSms;
    }

    public String getAceitaEmail() {
        return aceitaEmail;
    }

    public void setAceitaEmail(String aceitaEmail) {
        this.aceitaEmail = aceitaEmail;
    }

    public String getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(String dataNascimento) {
        this.dataNascimento = dataNascimento;
    }
}
