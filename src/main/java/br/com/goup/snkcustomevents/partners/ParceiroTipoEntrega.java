public class ParceiroTipoEntrega {

    private String idParceiroTipoEntrega;

    private String padrao;

    private String ativo;

    private Integer codigoParceiro;

    private String nomeTipoEntrega;

    private String idCadastrador;

    private String idAlterador;

    private String dataCadastro;

    private String dataAlteracao;

    private String nomeUsuarioComputador;

    private String ip;

    public String getIdParceiroTipoEntrega() {
        return idParceiroTipoEntrega;
    }

    public void setIdParceiroTipoEntrega(String idParceiroTipoEntrega) {
        this.idParceiroTipoEntrega = idParceiroTipoEntrega;
    }

    public String getPadrao() {
        return padrao;
    }

    public void setPadrao(String padrao) {
        this.padrao = padrao;
    }

    public String getAtivo() {
        return ativo;
    }

    public void setAtivo(String ativo) {
        this.ativo = ativo;
    }

    public Integer getCodigoParceiro() {
        return codigoParceiro;
    }

    public void setCodigoParceiro(Integer codigoParceiro) {
        this.codigoParceiro = codigoParceiro;
    }

    public String getNomeTipoEntrega() {
        return nomeTipoEntrega;
    }

    public void setNomeTipoEntrega(String nomeTipoEntrega) {
        this.nomeTipoEntrega = nomeTipoEntrega;
    }

    public String getIdCadastrador() {
        return idCadastrador;
    }

    public void setIdCadastrador(String idCadastrador) {
        this.idCadastrador = idCadastrador;
    }

    public String getIdAlterador() {
        return idAlterador;
    }

    public void setIdAlterador(String idAlterador) {
        this.idAlterador = idAlterador;
    }

    public String getDataCadastro() {
        return dataCadastro;
    }

    public void setDataCadastro(String dataCadastro) {
        this.dataCadastro = dataCadastro;
    }

    public String getDataAlteracao() {
        return dataAlteracao;
    }

    public void setDataAlteracao(String dataAlteracao) {
        this.dataAlteracao = dataAlteracao;
    }

    public String getNomeUsuarioComputador() {
        return nomeUsuarioComputador;
    }

    public void setNomeUsuarioComputador(String nomeUsuarioComputador) {
        this.nomeUsuarioComputador = nomeUsuarioComputador;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }
}
